package com.example.demo.cqrs.classes;

public class Contact {
	    private String type;
	    private String detail;
	    // getters and setters
		public String getType() {
			return type;
		}
		public void setType(String type) {
			this.type = type;
		}
		public String getDetail() {
			return detail;
		}
		public void setDetail(String detail) {
			this.detail = detail;
		}
	    
}
